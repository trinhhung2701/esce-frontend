interface StarIconProps extends IconProps {
  filled?: boolean
  half?: boolean
}

export const StarIcon = ({ className = '', filled = false, half = false, ...props }: StarIconProps) => {
  // Tạo ID duy nhất cho mỗi sao nửa để tránh conflict
  const clipId = useRef(`star-half-${Math.random().toString(36).substring(2, 9)}`).current

  if (half) {
    // Hiển thị sao nửa: nửa trái filled, nửa phải chỉ có viền
    return (
      <svg
        className={className}
        width="20"
        height="20"
        viewBox="0 0 24 24"
        fill="none"
        stroke="currentColor"
        strokeWidth="2"
        strokeLinecap="round"
        strokeLinejoin="round"
        {...props}
      >
        <defs>
          <clipPath id={clipId}>
            <rect x="0" y="0" width="12" height="24" />
          </clipPath>
        </defs>
        {/* Nửa sao bên trái - filled màu vàng */}
        <g clipPath={`url(#${clipId})`}>
          <polygon
            points="12 2 15.09 8.26 22 9.27 17 14.14 18.18 21.02 12 17.77 5.82 21.02 7 14.14 2 9.27 8.91 8.26 12 2"
            fill="currentColor"
          />
        </g>
        {/* Viền toàn bộ sao */}
        <polygon
          points="12 2 15.09 8.26 22 9.27 17 14.14 18.18 21.02 12 17.77 5.82 21.02 7 14.14 2 9.27 8.91 8.26 12 2"
          fill="none"
          stroke="currentColor"
        />
      </svg>
    )
  }

  return (
    <svg
      className={className}
      width="20"
      height="20"
      viewBox="0 0 24 24"
      fill={filled ? 'currentColor' : 'none'}
      stroke="currentColor"
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
      {...props}
    >
      <polygon points="12 2 15.09 8.26 22 9.27 17 14.14 18.18 21.02 12 17.77 5.82 21.02 7 14.14 2 9.27 8.91 8.26 12 2" />
    </svg>
  )
}

